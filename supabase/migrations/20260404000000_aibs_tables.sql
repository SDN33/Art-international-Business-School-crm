-- ---------------------------------------------------------------------------
-- 1. Extension de la table contacts (champs pipeline leads AIBS)
-- ---------------------------------------------------------------------------
alter table public.contacts
    add column if not exists pipeline_status      text,          -- Statut pipeline lead (Nouveau, Qualifié, Inscrit…)
    add column if not exists origine_lead         text,          -- Source (Meta Lead Ads, Organique, etc.)
    add column if not exists formation_souhaitee  text,          -- Formation demandée (texte libre + id slug)
    add column if not exists formation_slug       text,          -- Slug de la formation (doublage-voix, court-metrage…)
    add column if not exists utm_source           text,
    add column if not exists utm_medium           text,
    add column if not exists utm_campaign         text,
    add column if not exists calendly_reserved    boolean        not null default false,
    add column if not exists qualification_bot    boolean        not null default false,
    add column if not exists reponse_relance_email boolean       not null default false,
    add column if not exists reponse_relance_wa   boolean        not null default false,
    add column if not exists indice_no_show       integer        not null default 0,
    add column if not exists lien_calendly        text,
    add column if not exists valeur_estimee       numeric(10,2),
    add column if not exists converted_at         timestamp with time zone,
    add column if not exists airtable_id          text           unique;  -- dédup migration

-- Index pour recherche rapide sur le pipeline
create index if not exists contacts_pipeline_status_idx on public.contacts(pipeline_status);
create index if not exists contacts_airtable_id_idx     on public.contacts(airtable_id);

