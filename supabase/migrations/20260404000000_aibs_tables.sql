-- 12. Fonction RPC pour vérifier l'existence d'un email (pour n8n sans auth)
-- Permet au workflow n8n de faire un dédup sans clé service_role
-- ---------------------------------------------------------------------------
create or replace function public.check_contact_email_exists(p_email text)
returns boolean
language plpgsql
security definer
set search_path = public
as $$
begin
    return exists (
        select 1
        from public.contacts
        where email_jsonb @> jsonb_build_array(jsonb_build_object('email', p_email))
    );
end;
$$;

grant execute on function public.check_contact_email_exists(text) to anon;

