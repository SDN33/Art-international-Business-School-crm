-- 13. Vue paiements_summary – solde par inscription
-- ---------------------------------------------------------------------------
create or replace view public.inscriptions_summary with (security_invoker = on) as
select
    i.id,
    i.numero_inscription,
    i.date_inscription,
    i.contact_id,
    i.session_id,
    i.statut,
    i.mode_financement,
    i.montant_total,
    i.commentaires,
    i.date_validation,
    co.first_name  || ' ' || co.last_name as contact_nom,
    ts.session_name,
    f.nom          as formation_nom,
    f.slug         as formation_slug,
    coalesce(sum(p.montant) filter (where p.statut_paiement = 'Reçu'), 0) as montant_paye,
    i.montant_total - coalesce(sum(p.montant) filter (where p.statut_paiement = 'Reçu'), 0) as solde_restant,
    count(p.id) as nb_paiements
from public.inscriptions i
    left join public.contacts         co on co.id = i.contact_id
    left join public.training_sessions ts on ts.id = i.session_id
    left join public.formations        f  on f.id  = ts.formation_id
    left join public.paiements         p  on p.inscription_id = i.id
group by i.id, co.first_name, co.last_name, ts.session_name, f.nom, f.slug;
