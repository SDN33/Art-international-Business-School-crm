    id                  bigint generated by default as identity primary key,
    created_at          timestamp with time zone not null default now(),
    airtable_id         text unique,
    nom                 text not null,
    slug                text unique,
    sous_titre          text,
    description         text,
    domaine_artistique  text,
    duree_jours         numeric,
    duree_texte         text,
    lieu_principal      text,
    prix_catalogue      numeric(10,2),
    tarif               text,
    statut              text,
    date_texte          text,
    objectifs           text,
    intervenants_texte  text,
    format              text,
    points_forts        text,
    accroche            text,
    livrables           text,
    flexibilite         text,
    programme_json      jsonb,
    ordre               integer
);

create index if not exists formations_slug_idx   on public.formations(slug);
create index if not exists formations_statut_idx on public.formations(statut);

-- ---------------------------------------------------------------------------
