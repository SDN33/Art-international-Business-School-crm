);

create index if not exists training_sessions_formation_id_idx on public.training_sessions(formation_id);
create index if not exists training_sessions_start_date_idx   on public.training_sessions(start_date);

-- ---------------------------------------------------------------------------
-- 6. Tables de jonction many-to-many
-- ---------------------------------------------------------------------------
create table if not exists public.formation_intervenants (
    formation_id    bigint not null references public.formations(id)    on delete cascade,
    intervenant_id  bigint not null references public.intervenants(id)  on delete cascade,
    primary key (formation_id, intervenant_id)
);

create table if not exists public.session_intervenants (
    session_id      bigint not null references public.training_sessions(id) on delete cascade,
    intervenant_id  bigint not null references public.intervenants(id)      on delete cascade,
    primary key (session_id, intervenant_id)
);

-- ---------------------------------------------------------------------------
-- 7. Inscriptions – dossiers d'inscription d'un contact à une session
-- ---------------------------------------------------------------------------
